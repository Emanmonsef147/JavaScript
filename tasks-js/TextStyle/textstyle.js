var par = document.getElementById("PAR");

function ChangeFont(ff) {
    "use strict";
    par.style.fontFamily = ff;
    
}

function ChangeAlign(tt) {
    "use strict";
    par.style.textAlign = tt;
    
}

function ChangeHeight(ll) {
    "use strict";
    par.style.lineHeight = ll;
    
}

function ChangeLSpace(lt) {
    "use strict";
    par.style.letterSpacing = lt;
    
}

function ChangeIndent(ti) {
    "use strict";
    par.style.textIndent = ti;
    
}

function ChangeTransform(tr) {
    "use strict";
    par.style.textTransform = tr;
    
}

function ChangeDecorate(td) {
    "use strict";
    par.style.textDecoration = td;
    
}

function ChangeBorder(bb) {
    "use strict";
    par.style.borderStyle = bb;
    
}

function ChangeBorderColor(bc) {
    "use strict";
    par.style.borderColor = bc;
    
}